# A String (str) variable to store a player's name.
player_name = "Player 1"

# An Integer (int) variable to keep track of the score.
player_score = 0

# A Float (float) variable for something that needs decimal precision, like the ball's speed.
ball_speed = 7.5

# A Boolean (bool) variable to check if the game is running.
is_game_over = False

# Now, let's print the values stored in our variables.
print("Player:", player_name)
print("Score:", player_score)
print("Ball Speed:", ball_speed)
print("Is the game over?", is_game_over)
